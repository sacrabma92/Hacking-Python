

# Listas - Sintaxis []
lista = ["tarea1","tarea2", "tarea3"]
print(lista)
print(type(lista))

lista_numeros = [1,2,3,4,5]
print(lista_numeros)

lista_combinada = ["Tarea 1", 42, "Texto", [1,2,3,4,5]]
print(lista_combinada)

# Tuplas - Sintaxis () - Las tuplas son INMUTABLES
tupla = ("tarea 1", "tarea 2", "tarea 3")
print(tupla)
print(type(tupla))


# Diccionarios - Clave: Valor
diccionario = {
  "Tarea1": "Pendiente",
  "Tarea2": "Taerminada"
}
print(type(diccionario))
print(diccionario)